#include<bits/stdc++.h>
using namespace std;
int main(){
int a = 6, b = 10;
cout<<"(&) AND Operator \t "<< (a & b) << endl;
cout<<"(|) OR Operator  \t"<< (a | b) << endl;
cout<<"(^) XOR Operator \t"<< (a ^ b) << endl;
cout<<"(<<) LEFT-Shift Operator \t"<< (10 << 2) << endl;
cout<<"(>>) RIGHT-Shift Operator \t"<< (10 >> 1) << endl;
return 0;
}